export const WB_SHAPE_TOOLS = [
  { type: "line", label: "Line (L)", hotkey: "L", Icon: WbIconLine },
  { type: "arrow", label: "Arrow (A)", hotkey: "A", Icon: WbIconArrow },
  { type: "rectangle", label: "Rectangle (R)", hotkey: "R", Icon: WbIconRect },
  { type: "diamond", label: "Diamond (D)", hotkey: "D", Icon: WbIconDiamond },
  { type: "ellipse", label: "Ellipse (O)", hotkey: "O", Icon: WbIconEllipse },
] as const;

export type WbShapeToolType = (typeof WB_SHAPE_TOOLS)[number]["type"];

export function shapeIconFor(type: string, size = 16) {
  const found = WB_SHAPE_TOOLS.find((s) => s.type === type);
  const Icon = found?.Icon ?? WbIconLine;
  return <Icon size={size} />;
}
